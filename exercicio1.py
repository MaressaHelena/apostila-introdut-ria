# Exercício 1) O indice de Massa Corporal (IMC) é calculado dividindo-se o peso 
# de um individuo pelo quadrado de sua altura. Faça um programa que leia o peso e a altura
# de um individuo e informe seu IMC
peso = int(115)
altura = int(2)
IMC = (peso/(altura**2))
print("Peso:", peso)
print("Altura:", altura)
print("IMC:", IMC)